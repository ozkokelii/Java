package degiskenleer;

public class MatematikselIslemler {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c = 30;
		int d = 40;
		a++;
		b--;
		c += 10;
		d *= 2;
		
		System.out.println(++a);
		System.out.println(--b);
		System.out.println(c);
		System.out.println(d);
		 
		
	}

}
